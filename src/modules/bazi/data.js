// === 节气精确时间 ===
// 12个"节"的精确北京时间，用于确定月柱
// 顺序：立春(寅), 惊蛰(卯), 清明(辰), 立夏(巳), 芒种(午), 小暑(未),
//       立秋(申), 白露(酉), 寒露(戌), 立冬(亥), 大雪(子), 小寒(丑)
// 值为 UTC 毫秒时间戳（北京时间 = UTC+8）

function bjt(y, m, d, h, min) {
  // 北京时间 → UTC: 减8小时
  return Date.UTC(y, m - 1, d, h - 8, min);
}

