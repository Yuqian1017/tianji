// 节气近似算法（精度±1天）
export function approxJieqiDate(year, jieqiName) {
  const base = JIEQI_BASE[jieqiName];
  if (base === undefined) {
    console.warn(`approxJieqiDate: unknown jieqi "${jieqiName}"`);
    return null;
  }
  const Y = year - 2000;
  const day = base + 0.2422 * Y - Math.floor(Y / 4);

  // 确定月份
  const jieqiMonthMap = {
    '小寒':1,'大寒':1,'立春':2,'雨水':2,'惊蛰':3,'春分':3,
    '清明':4,'谷雨':4,'立夏':5,'小满':5,'芒种':6,'夏至':6,
    '小暑':7,'大暑':7,'立秋':8,'处暑':8,'白露':9,'秋分':9,
    '寒露':10,'霜降':10,'立冬':11,'小雪':11,'大雪':12,'冬至':12,
  };
  const m = jieqiMonthMap[jieqiName];
  const d = Math.floor(day);
  const h = Math.round((day - d) * 24);
  // 返回 UTC 时间戳（近似北京时间转UTC）
  return Date.UTC(year, m - 1, d, h - 8, 0);
}

