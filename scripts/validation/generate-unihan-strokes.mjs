import { createHash } from 'node:crypto';
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const [sourceArg, outputArg] = process.argv.slice(2);
if (!sourceArg || !outputArg) {
  throw new Error('Usage: node generate-unihan-strokes.mjs <Unihan_IRGSources.txt> <output.js>');
}

const sourcePath = resolve(sourceArg);
const outputPath = resolve(outputArg);
const source = readFileSync(sourcePath, 'utf8');
const sourceSha256 = createHash('sha256').update(source).digest('hex');
const values = new Map();

for (const line of source.split('\n')) {
  const match = line.match(/^U\+([0-9A-F]+)\tkTotalStrokes\t(\d+)$/);
  if (!match) continue;

  const codePoint = Number.parseInt(match[1], 16);
  if (codePoint < 0x4e00 || codePoint > 0x9fff) continue;
  values.set(codePoint, Number.parseInt(match[2], 10));
}

const expectedCount = 0x9fff - 0x4e00 + 1;
if (values.size !== expectedCount) {
  throw new Error(`Expected ${expectedCount} URO entries, found ${values.size}`);
}

const encodedEntries = [...values]
  .sort(([left], [right]) => left - right)
  .map(([codePoint, strokes]) => `${String.fromCodePoint(codePoint)}${strokes}`);
const chunks = [];
for (let index = 0; index < encodedEntries.length; index += 96) {
  chunks.push(encodedEntries.slice(index, index + 96).join(''));
}

const output = `// Generated by scripts/validation/generate-unihan-strokes.mjs. Do not edit manually.
// Source license: database/licenses/UNICODE_LICENSE_V3.txt

export const UNIHAN_STROKE_METADATA = Object.freeze({
  unicodeVersion: '17.0.0',
  property: 'kTotalStrokes',
  propertyStatus: 'informative',
  range: 'U+4E00-U+9FFF',
  entryCount: ${expectedCount},
  sourceUrl: 'https://www.unicode.org/Public/17.0.0/ucd/Unihan.zip',
  sourceSha256: '${sourceSha256}',
});

const ENCODED_STROKES = [
${chunks.map((chunk) => `  ${JSON.stringify(chunk)},`).join('\n')}
].join('');

const strokeMap = new Map();
for (const match of ENCODED_STROKES.matchAll(/([\\u4E00-\\u9FFF])(\\d{1,3})/gu)) {
  strokeMap.set(match[1], Number.parseInt(match[2], 10));
}

if (strokeMap.size !== UNIHAN_STROKE_METADATA.entryCount) {
  throw new Error(\`Invalid generated Unihan stroke table: \${strokeMap.size} entries\`);
}

export function getUnihanStrokeCount(char) {
  return strokeMap.get(char) ?? null;
}

export function getUnihanStrokeEntryCount() {
  return strokeMap.size;
}
`;

writeFileSync(outputPath, output, 'utf8');
console.log(JSON.stringify({
  outputPath,
  entryCount: values.size,
  sourceSha256,
}, null, 2));
